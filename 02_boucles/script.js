
for (let i = 0; i <= 10; i++) { 
  console.log(i);
}

let a = 0
while (a <= 10) {
  // do
  
  console.log(a)
  a++ // incrementation

  // fin du do
}

let cpt2 = 10
do {
  cpt2--; // decrementation
  console.log(cpt2);
} while (cpt2 > 0);


// cpt++ reviens à cpt = cpt + 1 ou cpt += 1
// cpt-- reviens à cpt = cpt - 1 ou cpt += 1

